#include "3-calc.h"

/**
 * op_add - adds;
 * @a: integer
 * @b: integer
 * Return: integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract;
 * @a : integer
 * @b: integer
 * Return: integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies;
 * @a: integer
 * @b: integer
 * Return: integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - op div;
 * @a: integer
 * @b: integer
 * Return: integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod func
 * @a: integer
 * @b: integer
 * Return: integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
